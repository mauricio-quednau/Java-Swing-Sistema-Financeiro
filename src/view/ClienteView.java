    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        String nome = jtfNomeRSocial.getText().toUpperCase();
        String cpfCnpj = jtfCpfCnpj.getText();
        String email = jtfEmail.getText();
        String fone = jtfFone.getText();
        String endereco = jtfEndereco.getText();

        String dataCadastro = jtfDataCadastro.getText();
        String usuario = jtfUsuario.getText();
        String senha = jtfSenha.getText();
        String obs = jtaObs.getText();

        String id = jtfId.getText();
        if(id.equals("")){ //campo vazio - insert
            Cliente cli = new Cliente(nome, cpfCnpj, email, fone, endereco, dataCadastro, obs, usuario,senha);
            new ClienteDAO().insert(cli);
        }else{ //campo preenchido - update
            Cliente cli = new Cliente(Integer.parseInt(id), nome, cpfCnpj, email, fone, endereco, dataCadastro, obs, usuario,senha);
            new ClienteDAO().update(cli);
        }

        controlarBotoes(0);
        limpaCampos();
        jtfDataCadastro.setText(new SimpleDateFormat("dd/MM/yyyy").format(new Date()));
        
        ClienteDAO cd = new ClienteDAO();
        clientes = (ArrayList) cd.list();
        preencherTabela(clientes);
        
        
    }//GEN-LAST:event_jbSalvarActionPerformed

