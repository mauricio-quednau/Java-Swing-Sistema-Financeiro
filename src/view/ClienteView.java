    public int getIdClienteLinhaTabela(int linha){
        if(linha < 0 || linha > jTable1.getRowCount())
            return 0;
        else{
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            return (int)model.getValueAt(linha, 0); // 0(zero) referente ao primeiro campo (ID) da tabela
        }
    }
    public Cliente getClienteLinhaTabela(int linha){
        if(linha < 0 || linha > jTable1.getRowCount())
            return null;
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Cliente c = new Cliente();
        for(int i=0; i<6; i++){
            switch(i){
                case 0:
                    c.setId((int)model.getValueAt(linha, i));
                    break;
                case 1:
                    c.setNomeRSocial((String)model.getValueAt(linha, i));
                    break;
                case 2:
                    c.setCpfCnpj((String)model.getValueAt(linha, i));
                    break;
                case 3:
                    c.setEmail((String)model.getValueAt(linha, i));
                    break;
                case 4:
                    c.setTelefone((String)model.getValueAt(linha, i));
                    break;
                case 5:
                    c.setEndereco((String)model.getValueAt(linha, i));
                    break;
            }
        }
        return c;
    }
    
    private void jbServicosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbServicosActionPerformed
        if(!jtfId.getText().equals("")){
            Cliente cli = null;
            for(int i = 0; i < clientes.size(); i++){
                if(clientes.get(i).getId() == Integer.parseInt(jtfId.getText())){
                    cli = clientes.get(i);
                    break;
                }
            }
            ClienteServicosView cs = new ClienteServicosView(super.getDesktopPane(), cli);
            cs.setVisible(true);
            parent.add(cs);
            parent.setComponentZOrder(cs, 0);
            
        }else{
            JOptionPane.showMessageDialog(this, "Selecione algum cliente!", "Serviços por cliente", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jbServicosActionPerformed

