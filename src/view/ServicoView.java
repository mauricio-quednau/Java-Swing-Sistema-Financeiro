    private void preencherTabela(ArrayList<Servico> servicos){
        DefaultTableModel modeloTb = (DefaultTableModel)jTable1.getModel();
        modeloTb.setNumRows(0);
        for(Servico s : servicos){
            modeloTb.addRow(new Object[]{s.getId(), s.getNome(), s.getDescricao(), s.getDataFromSQL()});
        }
    }
    
    private void setTamanhoColunasTabela(){
        jTable1.getColumnModel().getColumn(0).setMaxWidth(35);
        jTable1.getColumnModel().getColumn(1).setMaxWidth(300);
        jTable1.getColumnModel().getColumn(2).setMaxWidth(550);
        jTable1.getColumnModel().getColumn(3).setMaxWidth(100);
    }
    
    private void controlarBotoes(int estadoTela){
        switch(estadoTela){
            case 0: //inicial
                jbPesquisa.setEnabled(true);
                jbSalvar.setEnabled(true);
                jbCancelar.setEnabled(false);
                jbExcluir.setEnabled(false);
                break;
            case 1: //edição - com linha da tabela já selecionada
                jbSalvar.setEnabled(true);
                jbCancelar.setEnabled(true);
                jbExcluir.setEnabled(true);
                jbPesquisa.setEnabled(false);
                break;
            case 2: //Selecionou alguma linha da tabela
                jbExcluir.setEnabled(true);

        }
    }
    
    
    
    
    
    
    
    public void limpaCampos(){
        jtfId.setText("");
        jtfNome.setText("");
        jtfDataInclusao.setText("");
        jtaDescricao.setText("");
    }
    
    public int getIdServicoLinhaTabela(int linha){
        if(linha < 0 || linha > jTable1.getRowCount())
            return 0;
        else{
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            return (int)model.getValueAt(linha, 0); // 0(zero) referente ao primeiro campo (ID) da tabela
        }
    }
    public Servico getServicoLinhaTabela(int linha){
        if(linha < 0 || linha > jTable1.getRowCount())
            return null;
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Servico s = new Servico();
        for(int i=0; i<model.getColumnCount(); i++){
            switch(i){
                case 0:
                    s.setId((int)model.getValueAt(linha, i));
                    break;
                case 1:
                    s.setNome((String)model.getValueAt(linha, i));
                    break;
                case 2:
                    s.setDescricao((String)model.getValueAt(linha, i));
                    break;
                case 3:
                    s.setDataInclusao((String)model.getValueAt(linha, i));
                    break;
            }
        }
        return s;
    }
    
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // pegar a linha selecionada e jogar os dados correspondentes para cima
        // desabilitar botoes: editar e pesquisar - estado atual: edição
        // deixar habilitado: salvar, cancelar e excluir
        int linhaSelecionada = jTable1.getSelectedRow();
        if(linhaSelecionada >= 0){
            controlarBotoes(1);
            int idServico = getIdServicoLinhaTabela(linhaSelecionada);
            Servico s = null;
            for (Servico servico : servicos) {
                if (servico.getId() == idServico) {
                    s = servico;
                }
            }

            if(s != null){
                jtfId.setText(String.valueOf(s.getId()));
                jtfNome.setText(s.getNome());
                jtfDataInclusao.setText(s.getDataFromSQL());
                jtaDescricao.setText(s.getDescricao());
            }
        }
        boolean excluir = false;
        if(excluir){
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.removeRow(linhaSelecionada);
        }

        // ao carregar a tela, necessário deixar habilitado:
        // pesquisa, salvar (se nome diferente de vazio),
    }//GEN-LAST:event_jTable1MouseClicked

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        String id = jtfId.getText();
        if(id.equals(""))
            return;
        if(JOptionPane.showConfirmDialog(this, "Realmente excluir?") == 0){
            ServicoDAO sd = new ServicoDAO();
            sd.delete(Integer.parseInt(id));

            servicos = (ArrayList) sd.list();
            preencherTabela(servicos);
        }

        controlarBotoes(0);
        limpaCampos();
    }//GEN-LAST:event_jbExcluirActionPerformed

