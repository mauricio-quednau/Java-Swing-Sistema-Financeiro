    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        String nome = jtfNome.getText();
        String dataInclusao = jtfDataInclusao.getText();
        String descricao = jtaDescricao.getText();
        
        String id = jtfId.getText();
        ServicoDAO sd = new ServicoDAO();
        if(id.equals("")){ //campo vazio - insert
            Servico ser = new Servico(nome, descricao, dataInclusao);
            sd.insert(ser);
        }else{ //campo preenchido - update
            Servico ser = new Servico(Integer.parseInt(id), nome, descricao, dataInclusao);
            sd.update(ser);
        }

        controlarBotoes(0);
        limpaCampos();

        servicos = (ArrayList) sd.list();
        preencherTabela(servicos);
    }//GEN-LAST:event_jbSalvarActionPerformed

